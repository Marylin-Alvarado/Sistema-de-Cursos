    /**
     * Metodo para poder asignar la asistencia a un estudiante
     * Captura el numero de horas del estudiante y calcula el porcentaje usando el metodo de su controler
     */
    public void asignarAsistencia() {
        cursaC.getCursa().setHorasAsistidas(Integer.parseInt(txtHorasAsistidas.getText()));
        cursaC.calcularPorcentajeHorasAsistidas();
        System.out.println("Id estudiante  " + alumnController.getAlumno().getId());
        System.out.println("Nombre materia " + cursaC.getCursa().getAsignatura().getNombre());
        try {
            for (int i = 0; i < matriculaControler.getMatriculaList().getSize(); i++) {
                for (int j = 0; j < matriculaControler.getMatriculaList().obtener(i).getCursa().getSize(); j++) {
                    System.out.println("Materia " + matriculaControler.getMatriculaList().obtener(i).getCursa().obtener(j).getAsignatura().getNombre());
                    if (matriculaControler.getMatriculaList().obtener(i).getAlumno().getId() == alumnController.getAlumno().getId()
                            && matriculaControler.getMatriculaList().obtener(i).getCursa().obtener(j).getAsignatura().getNombre()
                            == cursaC.getCursa().getAsignatura().getNombre()) {
                        matriculaControler.getMatriculaList().obtener(i).getCursa().obtener(j).setHorasAsistidas(cursaC.getCursa().getHorasAsistidas());
                        matriculaControler.getMatriculaList().obtener(i).getCursa().obtener(j).setPorcentajeHorasAsistidas(cursaC.getCursa().getPorcentajeHorasAsistidas());
                        System.out.println("Porcentaje de sasistencia" + matriculaControler.getMatriculaList().obtener(i).getCursa().obtener(j).getPorcentajeHorasAsistidas());
                        System.out.println("Porcentaje de horas asistidas" + matriculaControler.getMatriculaList().obtener(i).getCursa().obtener(j).getHorasAsistidas());
                        Utilidades.modificarMatricula(matriculaControler.getMatriculaList().obtener(i), (matriculaControler.getMatriculaList().obtener(i).getIdMatricula() - 1));
                        JOptionPane.showMessageDialog(null, "Asistencia registrada");
                    }
                }
            }
        } catch (Exception e) {
            System.out.println("Error en asignar asistencia " + e);
            e.printStackTrace();
        }
    }

